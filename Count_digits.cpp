class Solution{
public:
    int evenlyDivides(int N){
        //code here
        int rem,count=0;
        int t=N;
        while(t)
        {
            rem=t%10;
            if(rem)
            {
                if(N%rem==0)
                count++;
            }
            t/=10;
        }
        return count;
    }
};

